def plusNumber(x,y):
    print(str(x),"+",str(y),"=",x+y)
def minusNumber(x,y):
    print(str(x), "-", str(y), "=" ,x - y)
def multiNumber(x,y):
    print(str(x), "*", str(y), "=", x * y)
def divNumber(x,y):
    print(str(x), "/", str(y), "=", x / y)
x=int(input("x="))
y=int(input("y="))
plusNumber(x,y)
minusNumber(x,y)
multiNumber(x,y)
divNumber(x,y)

